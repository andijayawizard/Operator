package com.andijaya.javafundamental.operator;

public class OperatorAritmatika {
    public static void aritmatika() {
        int val1=9;
        int val2=8;

        int hasilPenjumlahan=val1+val2;
        System.out.println("Hasil penjumlahan adalah "+hasilPenjumlahan);

        int hasilPengurangan=val1-val2;
        System.out.println("Hasil Pengurangan adalah "+hasilPengurangan);

        int hasilPerkalian=val1*val1;
        System.out.println("Hasil perkalian adalah "+hasilPerkalian);

    }
}
